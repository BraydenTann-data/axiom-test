/* ── STATE ──────────────────────────────────────────────────────── */
// 'all' means show everyone across all depts
let currentDept   = 'all';
let currentFilter = 'all';

/* ── FLATTEN all people into one array for easy lookup ─────────── */
const ALL_PEOPLE = Object.values(PEOPLE).flat();

/* ── HELPERS ────────────────────────────────────────────────────── */
function initials(name) {
  return name.split(' ').map(w => w[0]).join('').slice(0, 2).toUpperCase();
}
function isSenior(role) {
  return role === 'Managing Partner' || role === 'Partner' || role === 'Managing Director';
}

/* ── BUILD A CARD ELEMENT ───────────────────────────────────────── */
function buildCard(p) {
  const card = document.createElement('div');
  card.className = 'card';
  card.dataset.role = p.role;
  card.dataset.name = p.name.toLowerCase();
  card.dataset.dept = p.dept;

  const badgeClass  = isSenior(p.role) ? 'overlay-badge senior' : 'overlay-badge';
  const avatarInner = p.photo
    ? `<img src="${p.photo}" alt="${p.name}" />`
    : `<span class="avatar-initial">${initials(p.name)}</span>
       <span class="avatar-watermark">${initials(p.name)}</span>`;

  card.innerHTML = `
    <div class="avatar-bg" style="background:${p.color}">
      ${avatarInner}
    </div>
    <div class="card-label">
      <div class="card-name">${p.name}</div>
      <div class="card-role">${p.role}</div>
    </div>
    <div class="card-overlay">
      <div class="overlay-name">${p.name}</div>
      <div class="overlay-role">${p.role}</div>
      <span class="${badgeClass}">${p.role}</span>
      <div class="overlay-line"></div>
      <div class="overlay-bio">${p.bio}</div>
      <div class="overlay-dept">${p.dept}</div>
    </div>`;

  return card;
}

/* ── RENDER — always renders ALL people once into the grid ──────── */
function renderAll() {
  const grid = document.getElementById('teamGrid');
  grid.innerHTML = '';
  ALL_PEOPLE.forEach(p => grid.appendChild(buildCard(p)));
  filterCards();
}

/* ── FILTER — hides/shows cards based on dept + role + search ───── */
function filterCards() {
  const q = document.getElementById('search').value.toLowerCase().trim();
  let visible = 0;

  document.querySelectorAll('.card').forEach(c => {
    const deptMatch = currentDept === 'all' || c.dataset.dept === currentDept;
    const roleMatch = currentFilter === 'all' || c.dataset.role === currentFilter;
    const nameMatch = !q || c.dataset.name.includes(q) || c.dataset.role.toLowerCase().includes(q);

    const show = deptMatch && roleMatch && nameMatch;
    c.classList.toggle('hidden', !show);
    if (show) visible++;
  });

  document.getElementById('memberCount').textContent = visible + (visible === 1 ? ' person' : ' people');
  document.getElementById('emptyState').style.display = visible === 0 ? 'block' : 'none';
}

/* ── ROLE FILTER PILLS ──────────────────────────────────────────── */
function setFilter(f, el) {
  currentFilter = f;
  document.querySelectorAll('.pill').forEach(p => p.classList.remove('active'));
  el.classList.add('active');
  filterCards();
}

/* ── DEPT TABS — now just filter, don't re-render ───────────────── */
document.querySelectorAll('.dept-tab').forEach(tab => {
  tab.addEventListener('click', () => {
    // Map tab data-dept values to the dept string stored on each card
    const deptMap = {
      investment: 'Investment & Investor Partnership Services',
      finance:    'Finance & Admin',
      axiommind:  'Axiom Mind & Affiliates',
      all:        'all'
    };

    currentDept   = deptMap[tab.dataset.dept] || 'all';
    currentFilter = 'all';
    document.getElementById('search').value = '';

    document.querySelectorAll('.dept-tab').forEach(t => t.classList.remove('active'));
    tab.classList.add('active');
    document.querySelectorAll('.pill').forEach(p =>
      p.classList.toggle('active', p.dataset.filter === 'all')
    );

    filterCards();
  });
});

/* ── INIT ───────────────────────────────────────────────────────── */
renderAll();
